// Helper: get unique filter values
export function getFilterOptions() {
  const materials = [...new Set(products.map((p) => p.material))].sort();
  const technologies = [...new Set(products.map((p) => p.technology))].sort();
  const industries = [...new Set(products.flatMap((p) => p.industry))].sort();
  const sizes = ['Small', 'Medium', 'Large'] as const;
  const finishes = [...new Set(products.map((p) => p.finish))].sort();
  return { materials, technologies, industries, sizes, finishes };
}

export function getProductBySlug(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getFeaturedProducts(): Product[] {
  return products.filter((p) => p.featured);
}

// Technology badge colors
export function getTechColor(tech: Product['technology']): string {
  const colors: Record<string, string> = {
    FDM: 'bg-blue-500/20 text-blue-400 border-blue-500/30',
    SLA: 'bg-purple-500/20 text-purple-400 border-purple-500/30',
    Resin: 'bg-amber-500/20 text-amber-400 border-amber-500/30',
  };
  return colors[tech] || '';
}
